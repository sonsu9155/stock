<div>
	顺向的&ldquo;T+0&rdquo;操作技巧</div>
<div>
	&nbsp;</div>
<div>
	我国股市实行&ldquo;T+1&rdquo;交易制度以来，投资者已经不能采取&ldquo;T+0&rdquo;操作方式，但如果手中持有被套的股票，则仍可以变相的使用这种快捷的交易方式。具体方法是：先买入与被套个股同等数量的股票后，当天就将原先的被套股卖出，由于在获取当日股票差价利润的同时，持股数前后没有变化，等于完成了一次盘中&ldquo;T+0&rdquo;操作。这种操作方式通过经常性的赚取短线利润，达到最终降低持仓成本的目的。</div>
<div>
	&nbsp;</div>
<div>
	顺向的&ldquo;T+0&rdquo;操作属于超短线操作方式，有一定的操作难度和风险，具体应用时要掌握其操作原则：</div>
<div>
	&nbsp;</div>
<div>
	1、&ldquo;T+0&rdquo;操作必须建立在对个股的长期观察和多次模拟操作的基础上，能够非常熟悉个股股性和市场规律。</div>
<div>
	&nbsp;</div>
<div>
	2、&ldquo;T+0&rdquo;操作要求投资者必须有适时看盘的时间和条件。还要投资者有一定短线操作经验和快速的盘中应变能力。</div>
<div>
	&nbsp;</div>
<div>
	3、&ldquo;T+0&rdquo;操作时要快，不仅分析要快，决策要快，还要下单要快，跑道要快。这就要求投资者注意选择咨讯更新及时、交易方式迅速和交易费用低廉的证券公司。</div>
<div>
	&nbsp;</div>
<div>
	4、&ldquo;T+0&rdquo;操作时切忌不能贪心，一旦有所获利，或股价上行遇到阻力，立刻就落袋为安。这种操作事先不制定具体盈利目标，只以获取盘中震荡差价利润为操作目标。</div>
