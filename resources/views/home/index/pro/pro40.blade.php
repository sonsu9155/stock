        <h2>在股市T+1交易制度下如何做“T+0”</h2>
      </div>
      <div class="content"> <div>
	我们在&ldquo;T+1&rdquo;交易制度下，是可以用好用足手中筹码做&ldquo;T+0&rdquo;交易的。</div>
<div>
	&nbsp;</div>
<div>
	方法如下：</div>
<div>
	&nbsp;</div>
<div>
	一是利用手里股票、现金，借助盘中的波动，做&ldquo;T+0&rdquo;。当日买入手中已经有的股票品种，数量与原有同一品种的数量相同，也可不同，待其涨升到一定高度之后，将原有的同一品种全部或者部分卖出，从而实现当日的低买高卖、获取差价。实施这一操作，一般有下列益处：</div>
<div>
	&nbsp;</div>
<div>
	(1)如果原有品种是套牢的，则可以通过这一操作，吃到盘中的差价，争取早日降低成本、早日解套，甚至反败为胜。毕竟，假定按照一比一做&ldquo;T+0&rdquo;操作的话，操作成功之后，筹码还是那么多，但是账面上现金增加了，等于持有筹码的成本降低了。否则，一味死捂、被动等待，往往不知要等到猴年马月。被动解套不如主动解套。</div>
<div>
	&nbsp;</div>
<div>
	(2)如果不是套牢盘，而是获利盘，则双倍筹码的话，涨升中当天可能带来双倍的获利。</div>
<div>
	&nbsp;</div>
<div>
	但是，具体操作中，需要注意下列事项：</div>
<div>
	&nbsp;</div>
<div>
	(1)要求判断股市当日以及该股当日能否收出阳线，或者该股当日能否盘中创出高点。往往前市三十分钟的大盘走势，如果第三十分钟处于前三十分钟最高点的话，有利于利用盘中回荡做&ldquo;T+0&rdquo;操作。</div>
<div>
	&nbsp;</div>
<div>
	(2)要注意盘中同一板块是否有领头羊出现，这是比较确定的下手机会。如果有领头羊出现，并且领头羊冲击涨停板的话，则自己的同板块股票上，可能出现跟风性质的冲高动作，可以借此机会，即时介入、滞涨即走。</div>
<div>
	&nbsp;</div>
<div>
	(3)介入之后，注意同板块的领头羊能否封涨停板，如果不能，则一旦领头羊掉头，则跟风盘上的、用于&ldquo;T+0&rdquo;操作的原有筹码，毫不犹豫即时出局，以便保住几毛钱或者以上的短线获利。否则，出局不及时的话，跟风盘上的、用于&ldquo;T+0&rdquo;操作的新增筹码，有短线被粘被套的危险。</div>
<div>
	&nbsp;</div>
<div>
	二是利用手中已有筹码，做&ldquo;倒T+0&rdquo;，也即&ldquo;拔档子&rdquo;。一般用于中高位出现重大利空的时候。因为，即使是套牢筹码，即使是长线看好的品种，一旦在大盘遭遇重大利空的时候，尤其是股指、股价处于中高位的时候，往往可能出现中阴线、甚至长阴线的下跌机会。这时，即时逢高抛出，在股指、股价企稳之后逢低吸纳，则同一品种、高卖低买的话，可以吃到&ldquo;倒差价&rdquo;，从而达到筹码数量不变、账上现金增加的效果，或者达到总账不变、账上股票数量增加的目的，毕竟高位卖出之后、同样的钱低位可以买更多数量的同一股票。&ldquo;倒T+0&rdquo;，也即&ldquo;拔档子&rdquo;一般适用于上升途中的整理、洗盘的过程，不适用于大熊市。大熊市一般要空仓、长时间等待。</div>
