        <h2>什么是股票t+0交易</h2>
      </div>
      <div class="content"> <div>
	所谓的T+0的T，是指股票成交的当天日期。凡在股票成交当天办理好股票和价款清算交割手续的交易制度，就称为T+0交易。通俗地说，就是当天买入的股票在当天就可以卖出。T+0交易曾在中国实行过，但因为它的投机性太大，所以，自1995年1月1日起，为了保证股票市场的稳定，防止过度投机，股市改为实行&ldquo;T+1&rdquo;交易制度，当日买进的股票，要到下一个交易日才能卖出。</div>
<div>
	&nbsp;</div>
<div>
	T是英文Trade的首字母，是交易的意思。&ldquo;&rdquo;回转交易是指当日(T日)买入的股票可于成交后当日卖出其买入的全部或部分股票；若当日(T日)不卖出，T+1日或以后的任何一个交易日也可以进行卖出申报。就是说，在&ldquo;T+0&rdquo;回转交易条件下，申报买入股票确认成交后，不限制投资者在哪个交易日进行卖出申报，投资者既可以把当天买入的股票当天卖出，也可以用当天卖出股票返回的资金当天再买进股票。</div>
<div>
	&nbsp;</div>
<div>
	&ldquo;T+0&rdquo;回转交易能减少投资人的持仓风险、增强股票的流动性。&ldquo;T+0&rdquo;的缺点在于不能有效控制交易频率，过高的换手率会导致过度投机和市场的虚假繁荣，买空、卖空也难以控制，因此风险较大。</div>
<div>
	&nbsp;</div>
<div>
	T+0交易是深交所1993年底推出的一种交易办法，意思是，投资者买（卖）股票（或期货）当天确认成交后，当天买入股票可当天卖出，当天卖出股票又可当天买入的一种交易。1995年1月1日起，为了保证股票市场的稳定，防止过度投机，中国股市实行&quot;T+1&quot;交易制度，即当日买进的股票，必须要到下一个交易日才能卖出。</div>
<div>
	&nbsp;</div>
<div>
	同时，对资金仍然实行&ldquo;T+0&rdquo;，即当日回笼的资金马上可以使用。</div>
<div>
	&nbsp;</div>
<div>
	B股股票使用T+1，资金适用T+3。</div>
<div>
	&nbsp;</div>
<div>
	如没有用过T+0技巧操作的用户，建议可以用T+0模拟炒股平台进行体验和锻炼，积累T+0模拟炒股的经验，非常适合短线投机客的操作方式。</div>
<div>
	&nbsp;</div>
<div>
	当前中国沪、深证券交易所的国债、企业债、可转债、权证实行的是&ldquo;T+0&rdquo;交易制度；A股、基金实行的均是&ldquo;T+1&rdquo;的交易制度。</div>
<div>
	&nbsp;</div>
<div>
	交易制度对于市场的活跃程度有着重要的影响。在&ldquo;T+0&rdquo;的交易制度下，一笔资金可以多次交易、反复买卖，在不增加市场资金存量的情况下，有效地提高市场的流通性、活跃程度和交易量，可以产生明显的资金放大效应。在市况较弱的情况下，&ldquo;T+0&rdquo;交易制度一方面有利于减少投资者的投资风险，另一方面也将为投资者提供更多的短线交易机会，有助于投资者提高其盈利水平。在当前管理层已经实施&ldquo;证券交易佣金浮动制&rdquo;、投资者交易成本有所下降的情况下，也为实施&ldquo;T+0&rdquo;回转交易提供了必要的技术准备。实施&ldquo;T+0&rdquo;回转交易，还可为国家带来更多的印花税收入、为市场带来更多的短线机会、为券商带来更多的佣金收入，有利于&ldquo;多赢&rdquo;局面的形成，在一定程度上刺激当前交投清淡的弱市格局。</div>
<div>
	&nbsp;</div>
<div>
	但是，&ldquo;T+1&rdquo;到&ldquo;T+0&rdquo;的变革仅仅是交易制度上的一项技术性改革措施，尽管其对活跃市场具有一定的积极作用，但并不能从根本上改变市场牛市或熊市的根本特征，市场走势的根本好转仍然有待于上市公司业绩和整体宏观经济形势的改善。</div>
